const htmlChangeClass = document.querySelector('html');
const darkTheme = document.querySelector('.switcher__radio--dark');
const lightTheme = document.querySelector('.switcher__radio--light');

darkTheme.addEventListener('click', toggleThemeOnDark);
lightTheme.addEventListener('click', toggleThemeOnLight);
window.addEventListener('load', rememberSelectedTheme);

const localStorageKey = 'save-theme';
let savedTheme = '';


function toggleThemeOnDark() {
  htmlChangeClass.classList.add('dark');
  darkTheme.checked = true;
  localStorage.setItem(localStorageKey, 'dark');
}

function toggleThemeOnLight() {
  htmlChangeClass.classList.remove('dark');
  localStorage.removeItem(localStorageKey);
}

function rememberSelectedTheme() {
  savedTheme = localStorage.getItem(localStorageKey);
  if (savedTheme !== 'dark') {
    return;
  } else toggleThemeOnDark();
}
